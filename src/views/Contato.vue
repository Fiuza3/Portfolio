import { ref, reactive } from 'vue'
import { enviarEmailContato } from '@/services/emailService'

const enviando = ref(false)
const mensagemEnviada = ref(false)
const mensagemErro = ref('')

const formulario = reactive({
  nome: '',
  email: '',
  assunto: '',
  mensagem: ''
})

const enviarMensagem = async () => {
  enviando.value = true
  mensagemErro.value = ''
  
  try {
    const resultado = await enviarEmailContato(formulario)
    
    if (resultado.success) {
      // Reset do formulário
      Object.assign(formulario, {
        nome: '',
        email: '',
        assunto: '',
        mensagem: ''
      })
      
      mensagemEnviada.value = true
      
      // Esconder mensagem de sucesso após 5 segundos
      setTimeout(() => {
        mensagemEnviada.value = false
      }, 5000)
    } else {
      mensagemErro.value = resultado.message
    }
  } catch (error) {
    mensagemErro.value = 'Erro inesperado. Tente novamente.'
  } finally {
    enviando.value = false
  }
}
</script>